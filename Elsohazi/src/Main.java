    public static void egy(int a, int b, int c) 
    {
        int t[]={a,b,c};
        int i, j;
        for(i=0; i<t.length; i++)
         for(j=i+1;j<t.length; j++)
           if(t[i]>t[j]){
               int tmp= t[i];
               t[i]=t[j];
               t[j]=tmp;
           }
        System.out.println(t[0]+" "+t[1]+" "+t[2]);
        
    }
    
